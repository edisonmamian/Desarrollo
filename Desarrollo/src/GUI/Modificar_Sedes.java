    /*
    se encarga de obtener todos los campos con la informacion de la sede que se va a modificar
    */
    private void boton_modificar_sedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_modificar_sedeActionPerformed
        // TODO add your handling code here:
        /*
        se obtienen la informacion de los campos
        */
        String nombre = campo_consultar_sede.getText();
        String ciudad = campo_modificar_ciudad.getText();
        String direccion = campo_modificar_direccion.getText();
        String telefono = campo_modificar_telefono.getText();
        
        Sedes mod = new Sedes();
        //se hace el llamado a la funcion que realiza el cambio en la base de datos
        mod.cambiar(nombre, ciudad, direccion, telefono);
        /*
        vacia los campos
        */
        campo_consultar_sede.setText("");
        campo_modificar_ciudad.setText("");
        campo_modificar_direccion.setText("");
        campo_modificar_telefono.setText("");
    }//GEN-LAST:event_boton_modificar_sedeActionPerformed
