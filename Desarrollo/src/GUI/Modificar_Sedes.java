    /*
    se encarga de llamar la funcion buscar en la base de datos, con el nombre de la sede
    que se va a modificar
    */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        ArrayList <String> datos = new ArrayList<>();
        //se obtiene el nombre de la sede que se va a modificar
        String nombre = campo_consultar_sede.getText();
        Sedes reg = new Sedes();
        //se hace el llamado a la funcion que busca en la base de datos
        datos = reg.consultar(nombre);
        //se llenan los campos de texto con la informacion que se encuentra en la base de datos
        campo_consultar_sede.setText(datos.get(0));
        campo_modificar_ciudad.setText(datos.get(1));
        campo_modificar_direccion.setText(datos.get(2));
        campo_modificar_telefono.setText(datos.get(3));
    }//GEN-LAST:event_jButton3ActionPerformed

    private void campo_modificar_telefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campo_modificar_telefonoKeyTyped
        // TODO add your handling code here:
        
         if (!Character.isDigit(evt.getKeyChar()) && !Character.isISOControl(evt.getKeyChar())) {
            Toolkit.getDefaultToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_campo_modificar_telefonoKeyTyped

